# ─── Hour × Weekday Heatmap Report (v3.8.1) ──────────────────────────────────

def build_hour_dow_heatmap_report(
    rows: list[dict],
    period: str,
    layout: Optional[dict] = None,
    labels: Optional[dict] = None,
) -> dict[str, Any]:
    """
    Baut die Report-Datenstruktur für die Nutzungs-Heatmap (v3.8.1).
    Erwartet flache Rows aus query_hour_dow_heatmap: {hour, dow, total_pages, total_jobs}.
    """
    rows = rows or []
    layout = layout or {}

    # Aggregierte KPIs
    total_pages = sum(int(r.get("total_pages") or 0) for r in rows)
    total_jobs  = sum(int(r.get("total_jobs")  or 0) for r in rows)

    # Peak-Zelle ermitteln
    peak = None
    for r in rows:
        if peak is None or (int(r.get("total_pages") or 0) > int(peak.get("total_pages") or 0)):
            peak = r

    # Wochentagslabels ISO: 1=Sonntag..7=Samstag (SQL DATEFIRST 7).
    # Wir stellen die Zeilen in bekannter Lesereihenfolge Mo..So dar.
    dow_keys_order = [2, 3, 4, 5, 6, 7, 1]  # Mo, Di, Mi, Do, Fr, Sa, So
    dow_label_keys = {
        1: "rpt_eng_dow_sun",
        2: "rpt_eng_dow_mon",
        3: "rpt_eng_dow_tue",
        4: "rpt_eng_dow_wed",
        5: "rpt_eng_dow_thu",
        6: "rpt_eng_dow_fri",
        7: "rpt_eng_dow_sat",
    }
    row_labels = [_lbl(labels, dow_label_keys[k]) for k in dow_keys_order]
    col_labels = [f"{h:02d}" for h in range(24)]

    # Heatmap-Items: row=DOW-Label, col=Stunde, raw=total_pages
    items = []
    key_to_label = {k: _lbl(labels, dow_label_keys[k]) for k in dow_keys_order}
    for r in rows:
        dow = int(r.get("dow") or 0)
        hour = int(r.get("hour") or 0)
        if dow not in key_to_label:
            continue
        items.append({
            "row": key_to_label[dow],
            "col": f"{hour:02d}",
            "raw": float(r.get("total_pages") or 0),
        })

    heatmap = _build_chart(
        items,
        _lbl(labels, "rpt_eng_chart_hour_dow"),
        chart_type="heatmap",
        rows=row_labels,
        cols=col_labels,
        color_base=layout.get("primary_color", "#1976d2"),
        cell=24,
    )

    peak_hint = ""
    if peak and (peak.get("total_pages") or 0) > 0:
        peak_dow_label = key_to_label.get(int(peak.get("dow") or 0), "?")
        peak_hint = f"{peak_dow_label} {int(peak.get('hour') or 0):02d}:00"

    kpis = [
        {
            "label": _lbl(labels, "rpt_eng_kpi_total_pages"),
            "value": _fmt_num(total_pages),
            "sub": period,
        },
        {
            "label": _lbl(labels, "rpt_eng_kpi_total_jobs"),
            "value": _fmt_num(total_jobs),
            "sub": period,
        },
        {
            "label": _lbl(labels, "rpt_eng_kpi_peak_slot"),
            "value": peak_hint or "—",
            "sub": _fmt_num((peak or {}).get("total_pages") or 0),
        },
    ]

    sections = []
    if heatmap:
        sections.append({
            "title": _lbl(labels, "rpt_eng_chart_hour_dow"),
            "columns": [],
            "rows": [],
            "chart": heatmap,
        })

    # Optional: Top-5-Slots als Tabelle
    top5 = sorted(rows, key=lambda r: int(r.get("total_pages") or 0), reverse=True)[:5]
    if top5:
        t_rows = []
        for r in top5:
            dow = int(r.get("dow") or 0)
            t_rows.append([
                key_to_label.get(dow, "?"),
                f"{int(r.get('hour') or 0):02d}:00",
                _fmt_num(r.get("total_pages")),
                _fmt_num(r.get("total_jobs")),
            ])
        sections.append({
            "title": _lbl(labels, "rpt_eng_section_top_slots"),
            "columns": [
                _lbl(labels, "rpt_eng_col_weekday"),
                _lbl(labels, "rpt_eng_col_hour"),
                _lbl(labels, "rpt_eng_kpi_total_pages"),
                _lbl(labels, "rpt_eng_kpi_total_jobs"),
            ],
            "rows": t_rows,
        })

    return {"kpis": kpis, "sections": sections}


# ─── Ausgabe-Renderer ─────────────────────────────────────────────────────────

def _derive_density(density: str) -> tuple[int, int, int]:
    """Liefert (content_padding, section_gap, table_cell_py) für eine Density-Stufe."""
    return {
        "compact": (18, 14, 5),
        "normal":  (26, 22, 8),
        "airy":    (36, 32, 12),
    }.get(density, (26, 22, 8))


def _derive_logo_src(layout: dict) -> str:
    """
    Baut den Logo-src-Wert:
      - wenn layout.logo_base64 vorhanden → data-URI
      - sonst wenn layout.logo_url vorhanden → URL (legacy)
      - sonst ""
    """
    b64 = (layout.get("logo_base64") or "").strip()
    if b64:
        mime = layout.get("logo_mime", "image/png")
        # Wenn schon data-URI, nicht nochmal wrappen
        if b64.startswith("data:"):
            return b64
        return f"data:{mime};base64,{b64}"
    url = (layout.get("logo_url") or "").strip()
    return url


def render_html(
    title: str,
    period: str,
    report_data: dict[str, Any],
    layout: Optional[dict] = None,
    labels: Optional[dict] = None,
    lang: str = "en",
) -> str:
    """Rendert den Report als vollständige HTML-Seite (i18n + theme-aware)."""
    if not JINJA2_AVAILABLE:
        return _plain_html_fallback(title, period, report_data, layout)

    # Layout normalisieren (Defaults, Theme-Farben, Migration)
    layout = normalize_layout(layout or {})

    # Typography
    font_key  = layout.get("font_family", "arial")
    font_css  = FONTS_BY_KEY.get(font_key, FONTS_BY_KEY["arial"])["css"]

    # Density → CSS-Werte
    d_pad, d_gap, d_cell = _derive_density(layout.get("density", "normal"))

    # v3.9.2 — autoescape=True. Tabellenzellen werden damit automatisch
    # HTML-escaped (vorher konnte ein Dateiname wie '<img src=x onerror=…>'
    # in einem HTML-Report Skripte ausführen). SVG-Charts werden weiterhin
    # mit '| safe' gerendert, weil sie engine-intern erzeugt werden und
    # vertrauenswürdig sind.
    env = Environment(loader=BaseLoader(), autoescape=True)
    template = env.from_string(_HTML_TEMPLATE)
    return template.render(
        # i18n
        lang=lang,
        title=title,
        period=period,
        generated_at=datetime.now().strftime("%Y-%m-%d %H:%M"),
        period_label=_lbl(labels, "rpt_eng_period"),
        generated_label=_lbl(labels, "rpt_eng_generated_at"),
        no_data_label=_lbl(labels, "rpt_eng_no_data"),
        footer_default=_lbl(labels, "rpt_eng_footer_default"),

        # Branding
        company_name=layout.get("company_name", ""),
        footer_text=layout.get("footer_text", ""),
        logo_src=_derive_logo_src(layout),

        # Theme colors
        primary_color=layout.get("primary_color"),
        accent_color=layout.get("accent_color"),
        background_color=layout.get("background_color"),
        text_color=layout.get("text_color"),
        muted_color=layout.get("muted_color"),
        table_header_bg=layout.get("table_header_bg"),
        table_alt_bg=layout.get("table_alt_bg"),

        # Typography
        font_css=font_css,
        font_size_base=layout.get("font_size_base", 13),
        font_size_h1=layout.get("font_size_h1", 22),
        font_size_h2=layout.get("font_size_h2", 15),

        # Layout
        header_variant=layout.get("header_variant", "left"),
        density_padding=d_pad,
        density_gap=d_gap,
        density_cell_py=d_cell,

        # Charts
        charts_enabled=layout.get("charts_enabled", True),
        chart_style=layout.get("chart_style", "bars"),

        # Data
        kpis=report_data.get("kpis", []),
        sections=report_data.get("sections", []),
    )


def _plain_html_fallback(title, period, report_data, layout) -> str:
    """v3.9.2 — alle dynamischen Werte werden über html.escape() geführt,
    damit ein Dateiname/Username mit '<' oder Anführungszeichen den
    Plain-HTML-Fallback nicht in einen XSS-Vektor verwandelt."""
    import html as _h
    e = lambda v: _h.escape("" if v is None else str(v), quote=True)
    def _safe_cell(cell):
        """Delta-Markup durchlassen, alles andere escapen."""
        s = "" if cell is None else str(cell)
        if '<span class="delta-' in s:
            return s
        return _h.escape(s, quote=True)
    lines = [f"<html><body><h1>{e(title)}</h1><p>{e(period)}</p>"]
    for section in report_data.get("sections", []):
        lines.append(f"<h2>{e(section.get('title', ''))}</h2><table border='1'>")
        lines.append("<tr>" + "".join(f"<th>{e(c)}</th>" for c in section.get("columns", [])) + "</tr>")
        for row in section.get("rows", []):
            lines.append("<tr>" + "".join(f"<td>{_safe_cell(cell)}</td>" for cell in row) + "</tr>")
        lines.append("</table>")
    lines.append("</body></html>")
    return "\n".join(lines)


def render_csv(rows: list[dict]) -> str:
    """Rendert rohe Query-Ergebnisse als CSV."""
    if not rows:
        return ""
    output = io.StringIO()
    writer = csv.DictWriter(output, fieldnames=list(rows[0].keys()), extrasaction="ignore")
    writer.writeheader()
    for row in rows:
        writer.writerow({k: str(v) if v is not None else "" for k, v in row.items()})
    return output.getvalue()


def render_json(data: Any) -> str:
    """Serialisiert Daten als formatiertes JSON."""
    return json.dumps(data, indent=2, ensure_ascii=False, default=str)


def _strip_delta_html(text: str) -> str:
    """Entfernt die <span class="delta-*">…</span> HTML-Markierung für PDF/XLSX."""
    import re
    # <span class="delta-pos">▲ 12.3%</span>  →  ▲ 12.3%
    return re.sub(r'<[^>]+>', '', str(text))


def render_pdf(
    title: str,
    period: str,
    report_data: dict,
    layout: Optional[dict] = None,
    labels: Optional[dict] = None,
) -> bytes:
    """
    Erzeugt einen PDF-Report aus den Report-Daten (i18n + theme-aware).

    Verwendet fpdf2 mit Helvetica/Courier/Times (Latin-1) — Font-Key aus
    layout.font_family wird auf eine fpdf2-Familie gemappt. Alle Nicht-
    Latin1-Zeichen werden durch _pdf_safe() ersetzt.
    """
    try:
        from fpdf import FPDF
    except ImportError:
        logger.error("fpdf2 nicht installiert — PDF-Generierung nicht verfügbar")
        return b""

    layout = normalize_layout(layout or {})
    primary_color   = layout.get("primary_color", "#1a73e8")
    table_header_bg = layout.get("table_header_bg", primary_color)
    table_alt_hex   = layout.get("table_alt_bg", "#F9F9F9")

    # Font-Key → fpdf2 family
    font_key    = layout.get("font_family", "arial")
    font_family = FONTS_BY_KEY.get(font_key, FONTS_BY_KEY["arial"]).get("pdf_family", "Helvetica")

    # Hex → RGB
    def hex_to_rgb(h: str, fallback=(26, 115, 232)):
        try:
            h = (h or "").lstrip("#")
            if len(h) == 3:
                h = "".join(c * 2 for c in h)
            return tuple(int(h[i:i+2], 16) for i in (0, 2, 4))
        except Exception:
            return fallback

    r, g, b       = hex_to_rgb(primary_color)
    thr, thg, thb = hex_to_rgb(table_header_bg, (r, g, b))
    tar, tag, tab = hex_to_rgb(table_alt_hex, (249, 249, 249))

    pdf = FPDF()
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=15)

    # v3.7.9: Logo aus layout.logo_base64 einbetten (falls vorhanden).
    # Wird rechts oben in den farbigen Header gezeichnet, bevor der Text kommt.
    # fpdf2 akzeptiert BytesIO mit PNG/JPEG direkt (ab Version 2.5).
    logo_bytes = None
    try:
        b64 = (layout.get("logo_base64") or "").strip()
        if b64:
            if b64.startswith("data:"):
                # data:image/png;base64,<payload>
                _, _, _payload = b64.partition(",")
                b64 = _payload
            import base64 as _b64
            logo_bytes = _b64.b64decode(b64)
    except Exception as _e:
        logger.warning("Logo konnte nicht f\u00fcr PDF dekodiert werden: %s", _e)
        logo_bytes = None

    # Header-Hintergrund
    header_height = 19  # 12 (Titel) + 7 (Untertitel)
    pdf.set_fill_color(r, g, b)
    pdf.rect(0, pdf.get_y(), pdf.w, header_height, style="F")

    # Logo rechts oben im Header
    if logo_bytes:
        try:
            import io as _io
            logo_h = 14
            logo_w = 28   # max Breite, fpdf skaliert Aspect Ratio automatisch wenn h gesetzt
            logo_x = pdf.w - pdf.r_margin - logo_w
            logo_y = pdf.get_y() + (header_height - logo_h) / 2
            pdf.image(_io.BytesIO(logo_bytes), x=logo_x, y=logo_y, h=logo_h)
        except Exception as _e:
            logger.warning("Logo-Embedding im PDF fehlgeschlagen: %s", _e)

    # Header-Text
    pdf.set_text_color(255, 255, 255)
    pdf.set_font(font_family, "B", 16)
    pdf.cell(0, 12, _pdf_safe(title), ln=True, fill=False, align="L")
    pdf.set_font(font_family, "", 9)
    period_lbl    = _lbl(labels, "rpt_eng_period")
    generated_lbl = _lbl(labels, "rpt_eng_generated_at")
    pdf.cell(0, 7, _pdf_safe(f"{period_lbl}: {period}  |  {generated_lbl}: {datetime.now().strftime('%Y-%m-%d %H:%M')}"),
             ln=True, fill=False, align="L")
    pdf.ln(4)

    # KPIs
    kpis = report_data.get("kpis", [])
    if kpis:
        pdf.set_fill_color(240, 244, 255)
        pdf.set_text_color(0, 0, 0)
        pdf.set_font(font_family, "B", 10)
        pdf.cell(0, 6, _pdf_safe(_lbl(labels, "rpt_eng_section_summary")), ln=True)
        pdf.ln(2)
        for kpi in kpis:
            pdf.set_font(font_family, "B", 11)
            pdf.set_text_color(r, g, b)
            pdf.cell(0, 7, _pdf_safe(f"  {kpi.get('label','')}: {kpi.get('value','')}"), ln=True, fill=True)
            if kpi.get("sub"):
                pdf.set_font(font_family, "", 9)
                pdf.set_text_color(120, 120, 120)
                pdf.cell(0, 5, _pdf_safe(f"    {kpi['sub']}"), ln=True)
        pdf.ln(4)

    # Sections / Tabellen
    pdf.set_text_color(0, 0, 0)
    for section in report_data.get("sections", []):
        pdf.set_font(font_family, "B", 12)
        pdf.set_text_color(r, g, b)
        pdf.cell(0, 8, _pdf_safe(section.get("title", "")), ln=True)
        pdf.set_text_color(0, 0, 0)
        pdf.ln(1)

        columns = section.get("columns", [])
        rows = section.get("rows", [])
        if not rows:
            pdf.set_font(font_family, "I", 9)
            pdf.set_text_color(150, 150, 150)
            pdf.cell(0, 6, _pdf_safe(_lbl(labels, "rpt_eng_no_data")), ln=True)
            pdf.ln(3)
            continue

        # Spaltenbreiten gleichmäßig verteilen
        page_w = pdf.w - 2 * pdf.l_margin
        col_w = page_w / max(len(columns), 1)

        # Tabellenkopf
        pdf.set_fill_color(thr, thg, thb)
        pdf.set_text_color(255, 255, 255)
        pdf.set_font(font_family, "B", 8)
        for col in columns:
            pdf.cell(col_w, 7, _pdf_safe(str(col)), border=0, fill=True)
        pdf.ln()

        # Tabellenzeilen
        pdf.set_font(font_family, "", 8)
        for i, row in enumerate(rows):
            if i % 2 == 0:
                pdf.set_fill_color(tar, tag, tab)
            else:
                pdf.set_fill_color(255, 255, 255)
            pdf.set_text_color(0, 0, 0)
            for cell in row:
                # Delta-HTML aus trend-Report strippen
                text = _strip_delta_html(cell)
                pdf.cell(col_w, 6, _pdf_safe(str(text)), border=0, fill=True)
            pdf.ln()
        pdf.ln(4)

    return bytes(pdf.output())


def render_xlsx(
    title: str,
    period: str,
    report_data: dict,
    layout: Optional[dict] = None,
    labels: Optional[dict] = None,
) -> bytes:
    """
    Erzeugt einen Excel-Report (XLSX) aus den Report-Daten (i18n + theme-aware).
    Verwendet openpyxl.
    """
    try:
        import openpyxl
        from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
    except ImportError:
        logger.error("openpyxl nicht installiert — XLSX-Generierung nicht verfügbar")
        return b""

    layout = normalize_layout(layout or {})

    def _hex(h: str, fallback: str) -> str:
        h = (h or "").lstrip("#")
        if len(h) == 3:
            h = "".join(c * 2 for c in h)
        if len(h) != 6:
            return fallback
        return h.upper()

    header_hex  = _hex(layout.get("table_header_bg"), "1A73E8")
    alt_hex     = _hex(layout.get("table_alt_bg"),    "F9F9F9")

    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "Report"

    header_fill = PatternFill("solid", fgColor=header_hex)
    header_font = Font(bold=True, color="FFFFFF", size=10)
    kpi_font    = Font(bold=True, size=11)
    thin_border = Border(bottom=Side(style="thin", color="DDDDDD"))

    # Logo einbetten (falls vorhanden) — oben rechts, Ankerzelle E1
    try:
        b64 = (layout.get("logo_base64") or "").strip()
        if b64:
            if b64.startswith("data:"):
                _, _, _payload = b64.partition(",")
                b64 = _payload
            import base64 as _b64
            logo_bytes = _b64.b64decode(b64)
            from openpyxl.drawing.image import Image as _XlImg
            _img = _XlImg(io.BytesIO(logo_bytes))
            # Hoehe auf ca. 50px begrenzen, Seitenverhaeltnis beibehalten
            try:
                if _img.height and _img.width:
                    _ratio = _img.width / float(_img.height)
                    _img.height = 50
                    _img.width  = int(50 * _ratio)
            except Exception:
                pass
            _img.anchor = "E1"
            ws.add_image(_img)
    except Exception as _e:
        logger.warning("Logo-Embedding im XLSX fehlgeschlagen: %s", _e)

    row_idx = 1

    # Titel
    ws.cell(row_idx, 1, title)
    ws.cell(row_idx, 1).font = Font(bold=True, size=14)
    row_idx += 1
    ws.cell(
        row_idx, 1,
        f"{_lbl(labels, 'rpt_eng_period')}: {period}  |  "
        f"{_lbl(labels, 'rpt_eng_generated_at')}: {datetime.now().strftime('%Y-%m-%d %H:%M')}"
    )
    ws.cell(row_idx, 1).font = Font(italic=True, size=9, color="888888")
    row_idx += 2

    # KPIs
    kpis = report_data.get("kpis", [])
    if kpis:
        ws.cell(row_idx, 1, _lbl(labels, "rpt_eng_section_summary"))
        ws.cell(row_idx, 1).font = Font(bold=True, size=11)
        row_idx += 1
        for kpi in kpis:
            ws.cell(row_idx, 1, kpi.get("label", ""))
            ws.cell(row_idx, 2, kpi.get("value", ""))
            ws.cell(row_idx, 2).font = kpi_font
            if kpi.get("sub"):
                ws.cell(row_idx, 3, kpi["sub"])
                ws.cell(row_idx, 3).font = Font(size=9, color="888888")
            row_idx += 1
        row_idx += 1

    # Sections / Tabellen
    for section in report_data.get("sections", []):
        ws.cell(row_idx, 1, section.get("title", ""))
        ws.cell(row_idx, 1).font = Font(bold=True, size=12)
        row_idx += 1

        columns = section.get("columns", [])
        rows    = section.get("rows", [])

        if columns:
            for col_idx, col in enumerate(columns, start=1):
                cell = ws.cell(row_idx, col_idx, col)
                cell.fill = header_fill
                cell.font = header_font
                cell.alignment = Alignment(horizontal="left")
            row_idx += 1

        alt_fill = PatternFill("solid", fgColor=alt_hex)
        for i, row in enumerate(rows):
            for col_idx, cell_val in enumerate(row, start=1):
                # Delta-HTML aus trend-Report strippen
                val = _strip_delta_html(cell_val) if isinstance(cell_val, str) else cell_val
                cell = ws.cell(row_idx, col_idx, val)
                cell.border = thin_border
                if i % 2 == 0:
                    cell.fill = alt_fill
            row_idx += 1
        row_idx += 2

    # Spaltenbreiten anpassen
    for col in ws.columns:
        max_len = 0
        col_letter = col[0].column_letter
        for cell in col:
            try:
                if cell.value:
                    max_len = max(max_len, len(str(cell.value)))
            except Exception:
                pass
        ws.column_dimensions[col_letter].width = min(max_len + 4, 40)

    buf = io.BytesIO()
    wb.save(buf)
    return buf.getvalue()


def generate_report(
    query_type: str,
    data: Any,
    period: str,
    layout: Optional[dict] = None,
    output_formats: Optional[list[str]] = None,
    currency: str = "€",
    labels: Optional[dict] = None,
    lang: str = "en",
    query_params: Optional[dict] = None,
) -> dict[str, str]:
    """
    Erzeugt alle angeforderten Ausgabeformate für einen Report (i18n-aware).

    Args:
        query_type:     "print_stats" | "cost_report" | "top_users" | "top_printers" |
                        "trend" | "anomalies"
        data:           Query-Ergebnis (Liste oder Dict für trend)
        period:         Zeitraum-Label
        layout:         Layout/Theme-Dict (wird durch normalize_layout() normalisiert)
        output_formats: Liste: "html" | "csv" | "json" | "pdf" | "xlsx"
        currency:       Währungssymbol (für Kosten-Formatierung)
        labels:         i18n-Dict mit rpt_eng_*-Keys (aus TRANSLATIONS[locale])
        lang:           ISO-Sprachcode (für `<html lang>`)

    Returns:
        Dict {format: content}
    """
    output_formats = output_formats or ["html"]
    layout = normalize_layout(layout or {})
    query_params = query_params or {}
    group_by = str(query_params.get("group_by") or "day")

    # Währung aus Layout überschreiben wenn gesetzt (Theme-Config)
    cur_key = layout.get("currency")
    if cur_key and cur_key in CURRENCIES_BY_KEY:
        currency = CURRENCIES_BY_KEY[cur_key]["symbol"]

    # Report-Datenstruktur bauen
    if query_type == "print_stats":
        report_data = build_print_stats_report(
            data, period, layout, group_by=group_by, labels=labels)
        title = _lbl(labels, "rpt_eng_title_print_stats")
    elif query_type == "cost_report":
        report_data = build_cost_report_report(
            data, period, layout, currency, group_by=group_by, labels=labels)
        title = _lbl(labels, "rpt_eng_title_cost_report")
    elif query_type == "top_users":
        report_data = build_top_users_report(data, period, currency, labels=labels)
        title = _lbl(labels, "rpt_eng_title_top_users")
    elif query_type == "top_printers":
        report_data = build_top_printers_report(data, period, currency, labels=labels)
        title = _lbl(labels, "rpt_eng_title_top_printers")
    elif query_type == "trend":
        report_data = build_trend_report(data, currency, labels=labels)
        title = _lbl(labels, "rpt_eng_title_trend")
    elif query_type == "hour_dow_heatmap":
        report_data = build_hour_dow_heatmap_report(data, period, layout, labels=labels)
        title = _lbl(labels, "rpt_eng_title_hour_dow_heatmap")
    else:
        # Stufe 2: Generische Tabellen-Ausgabe (Spalten aus erster Zeile).
        # v3.7.9: i18n-Titel aus rpt_eng_title_<type> wenn vorhanden, sonst
        # humanisiertes Fallback \u2014 so bekommt z.B. service_desk einen
        # vern\u00fcnftigen Titel in der jeweiligen UI-Sprache.
        # v3.7.10: DB-Spaltennamen mit _translate_col(lang) \u00fcbersetzen, damit
        # im HTML/PDF/XLSX keine rohen Namen wie "job_id", "print_time" mehr
        # auftauchen.
        _keys_raw = list(data[0].keys()) if data else []
        _keys_loc = [_translate_col(k, lang) for k in _keys_raw]
        report_data = {
            "kpis": [],
            "sections": [{
                "title":   _lbl(labels, "rpt_eng_section_details"),
                "columns": _keys_loc,
                "rows":    [[str(v) for v in r.values()] for r in data] if data else [],
            }],
        }
        title_key = f"rpt_eng_title_{query_type}"
        if labels and title_key in labels:
            title = labels[title_key]
        else:
            title = query_type.replace("_", " ").title()

    # Optional: Environmental Impact Section
    if layout.get("show_env_impact"):
        totals = report_data.get("_totals", {})
        total_pages = totals.get("total_pages", 0)
        saved = totals.get("saved_sheets", 0)
        if total_pages > 0:
            env = compute_env_impact(total_pages, saved_sheets=saved)
            report_data.setdefault("sections", []).append(
                _build_env_impact_section(env, labels=labels)
            )

    # Gewünschte Formate generieren
    company = layout.get("company_name", "").strip()
    title_full = f"{company} – {title}" if company else title
    results: dict[str, Any] = {}

    if "html" in output_formats:
        results["html"] = render_html(title_full, period, report_data, layout, labels=labels, lang=lang)
    if "csv" in output_formats:
        raw = data if isinstance(data, list) else []
        _csv = render_csv(raw)
        if not _csv:
            hint = _lbl(labels, "rpt_eng_no_data")
            results["csv"] = f'{_lbl(labels, "rpt_eng_period")},Info\r\n"{period}","{hint}"\r\n'
        else:
            results["csv"] = _csv
    if "json" in output_formats:
        results["json"] = render_json(data)
    if "pdf" in output_formats:
        try:
            results["pdf"] = render_pdf(title_full, period, report_data, layout, labels=labels)
        except Exception as e:
            logger.error("PDF-Generierung fehlgeschlagen: %s", e, exc_info=True)
            results["pdf_error"] = str(e)
    if "xlsx" in output_formats:
        try:
            results["xlsx"] = render_xlsx(title_full, period, report_data, layout, labels=labels)
        except Exception as e:
            logger.error("XLSX-Generierung fehlgeschlagen: %s", e, exc_info=True)
            results["xlsx_error"] = str(e)

    return results
