def _build_chart(
    items: list[dict],
    title: str,
    max_items: int = 20,
    chart_type: str = "bar",
    **kwargs,
) -> dict:
    """
    Generische Chart-Fabrik mit Type-Dispatcher (v3.8.1).

    Unterstützte `chart_type`-Werte:
      - "bar"       Horizontale CSS-Balken (Default, rückwärtskompatibel)
      - "line"      Inline-SVG Liniendiagramm (für Zeitreihen)
      - "heatmap"   Inline-SVG 2D-Heatmap (z.B. Stunde × Wochentag)
      - "donut"     Inline-SVG Donut/Ring-Chart (für Proportionen)
      - "sparkline" Winziger Inline-SVG-Trend (z.B. in KPI-Karten)

    Args:
        items:      Liste mit {"label": str, "display": str, "raw": float}
                    oder (für heatmap) beliebige Matrix-Struktur via kwargs.
        title:      Diagrammtitel
        max_items:  Maximale Anzahl Einträge
        chart_type: Dispatcher-Key
        **kwargs:   Chart-spezifische Optionen (color, size, ...)

    Returns:
        dict mit mindestens {"type": str, "title": str}
        + typspezifischen Feldern:
          - bar:       "bars"  (Liste)
          - line/heatmap/donut/sparkline: "svg" (str, inline SVG markup)
        oder {} wenn keine Daten.
    """
    chart_type = (chart_type or "bar").lower()
    if chart_type == "line":
        return _build_chart_svg_line(items, title, max_items, **kwargs)
    if chart_type == "heatmap":
        return _build_chart_svg_heatmap(items, title, **kwargs)
    if chart_type == "donut":
        return _build_chart_svg_donut(items, title, **kwargs)
    if chart_type == "sparkline":
        return _build_chart_svg_sparkline(items, **kwargs)
    # Default: bar (backwards-compatible)
    return _build_chart_bars(items, title, max_items)


def _build_chart_bars(items: list[dict], title: str, max_items: int = 20) -> dict:
    """Horizontale CSS-Balken (wie v3.8.0 und früher)."""
    items = [i for i in items if (i.get("raw") or 0) > 0]
    if not items:
        return {}
    items = items[:max_items]
    max_val = max(i.get("raw", 0) or 0 for i in items) or 1
    return {
        "type": "bar",
        "title": title,
        "bars": [
            {
                "label": str(i["label"]),
                "value": str(i["display"]),
                "pct":   max(1, round((i.get("raw", 0) or 0) / max_val * 100)),
            }
            for i in items
        ],
    }


def _build_chart_svg_line(
    items: list[dict],
    title: str,
    max_items: int = 60,
    width: int = 640,
    height: int = 180,
    color: str = "#1976d2",
    fill: str = "#1976d244",
) -> dict:
    """
    Inline-SVG Liniendiagramm für Zeitreihen.
    items: [{"label", "display", "raw"}, ...]
    """
    items = [i for i in items if i.get("raw") is not None]
    if len(items) < 2:
        return {}
    items = items[:max_items]
    vals = [float(i.get("raw") or 0) for i in items]
    vmax = max(vals) or 1.0
    vmin = min(vals)
    span = (vmax - vmin) or 1.0
    pad_l, pad_r, pad_t, pad_b = 36, 12, 12, 28
    w, h = width - pad_l - pad_r, height - pad_t - pad_b
    n = len(vals)
    step = w / max(n - 1, 1)
    pts = []
    for idx, v in enumerate(vals):
        x = pad_l + idx * step
        y = pad_t + h - ((v - vmin) / span) * h
        pts.append((x, y))
    poly = " ".join(f"{x:.1f},{y:.1f}" for x, y in pts)
    area_poly = (
        f"{pts[0][0]:.1f},{pad_t + h:.1f} "
        + poly
        + f" {pts[-1][0]:.1f},{pad_t + h:.1f}"
    )
    # Y-Achse: min/max Label
    y_label_max = f"{vmax:,.0f}".replace(",", ".")
    y_label_min = f"{vmin:,.0f}".replace(",", ".")
    # X-Achse: first/mid/last Label
    def _x_label(i):
        return (
            str(items[i].get("label", "") or "")[:10]
        )
    x_first = _x_label(0)
    x_mid   = _x_label(n // 2)
    x_last  = _x_label(n - 1)
    svg = (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="100%" viewBox="0 0 {width} {height}" '
        f'style="max-width:{width}px;font-family:-apple-system,Segoe UI,Roboto,sans-serif;">'
        f'<rect x="{pad_l}" y="{pad_t}" width="{w}" height="{h}" fill="#fafafa" stroke="#e0e0e0"/>'
        # horizontal gridlines (0.25 / 0.5 / 0.75)
        f'<line x1="{pad_l}" y1="{pad_t + h*0.25:.1f}" x2="{pad_l+w}" y2="{pad_t + h*0.25:.1f}" stroke="#eee"/>'
        f'<line x1="{pad_l}" y1="{pad_t + h*0.50:.1f}" x2="{pad_l+w}" y2="{pad_t + h*0.50:.1f}" stroke="#eee"/>'
        f'<line x1="{pad_l}" y1="{pad_t + h*0.75:.1f}" x2="{pad_l+w}" y2="{pad_t + h*0.75:.1f}" stroke="#eee"/>'
        f'<polygon points="{area_poly}" fill="{fill}" stroke="none"/>'
        f'<polyline points="{poly}" fill="none" stroke="{color}" stroke-width="2" stroke-linejoin="round" stroke-linecap="round"/>'
    )
    # Datenpunkte
    for x, y in pts:
        svg += f'<circle cx="{x:.1f}" cy="{y:.1f}" r="2.5" fill="{color}"/>'
    # Y-Achsenbeschriftung
    svg += (
        f'<text x="{pad_l - 4}" y="{pad_t + 10}" font-size="10" fill="#666" text-anchor="end">{y_label_max}</text>'
        f'<text x="{pad_l - 4}" y="{pad_t + h}" font-size="10" fill="#666" text-anchor="end">{y_label_min}</text>'
    )
    # X-Achsenbeschriftung
    svg += (
        f'<text x="{pad_l}" y="{height - 8}" font-size="10" fill="#666" text-anchor="start">{x_first}</text>'
        f'<text x="{pad_l + w/2:.1f}" y="{height - 8}" font-size="10" fill="#666" text-anchor="middle">{x_mid}</text>'
        f'<text x="{pad_l + w}" y="{height - 8}" font-size="10" fill="#666" text-anchor="end">{x_last}</text>'
    )
    svg += "</svg>"
    return {"type": "svg", "variant": "line", "title": title, "svg": svg}


def _build_chart_svg_heatmap(
    items: list[dict],
    title: str,
    rows: Optional[list[str]] = None,
    cols: Optional[list[str]] = None,
    cell: int = 28,
    color_base: str = "#1976d2",
) -> dict:
    """
    Inline-SVG 2D-Heatmap.
    items: [{"row": str|int, "col": str|int, "raw": float}, ...]
    rows / cols: optional explizite Achsenreihenfolge.
    """
    if not items:
        return {}
    rs = rows or sorted({str(i.get("row", "")) for i in items})
    cs = cols or sorted({str(i.get("col", "")) for i in items})
    vmax = max((float(i.get("raw") or 0) for i in items), default=0) or 1.0
    grid = {(str(i.get("row", "")), str(i.get("col", ""))): float(i.get("raw") or 0) for i in items}
    pad_l, pad_t, pad_r, pad_b = 80, 24, 12, 20
    w = pad_l + len(cs) * cell + pad_r
    h = pad_t + len(rs) * cell + pad_b
    svg = (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="100%" viewBox="0 0 {w} {h}" '
        f'style="max-width:{w}px;font-family:-apple-system,Segoe UI,Roboto,sans-serif;">'
    )
    # Spaltenbeschriftung (oben)
    for ci, c in enumerate(cs):
        cx = pad_l + ci * cell + cell / 2
        svg += f'<text x="{cx:.1f}" y="{pad_t - 6}" font-size="10" fill="#666" text-anchor="middle">{c}</text>'
    # Zeilenbeschriftung (links) + Zellen
    for ri, r in enumerate(rs):
        ry = pad_t + ri * cell + cell / 2 + 4
        svg += f'<text x="{pad_l - 6}" y="{ry:.1f}" font-size="10" fill="#666" text-anchor="end">{r}</text>'
        for ci, c in enumerate(cs):
            v = grid.get((r, c), 0.0)
            opacity = min(1.0, v / vmax)
            x = pad_l + ci * cell
            y = pad_t + ri * cell
            svg += (
                f'<rect x="{x}" y="{y}" width="{cell-1}" height="{cell-1}" '
                f'fill="{color_base}" fill-opacity="{opacity:.2f}" stroke="#fff" stroke-width="1"/>'
            )
            if v >= vmax * 0.4:
                label_color = "#fff" if opacity > 0.6 else "#333"
                svg += (
                    f'<text x="{x + cell/2 - 0.5:.1f}" y="{y + cell/2 + 3.5:.1f}" '
                    f'font-size="9" fill="{label_color}" text-anchor="middle">{int(v)}</text>'
                )
    svg += "</svg>"
    return {"type": "svg", "variant": "heatmap", "title": title, "svg": svg}


def _build_chart_svg_donut(
    items: list[dict],
    title: str,
    size: int = 200,
    palette: Optional[list[str]] = None,
) -> dict:
    """
    Inline-SVG Donut-Chart für Proportionen.
    items: [{"label", "display", "raw"}, ...]
    """
    items = [i for i in items if (i.get("raw") or 0) > 0]
    if not items:
        return {}
    total = sum(float(i.get("raw") or 0) for i in items) or 1.0
    palette = palette or [
        "#1976d2", "#f57c00", "#388e3c", "#7b1fa2",
        "#c62828", "#00796b", "#fbc02d", "#5d4037",
    ]
    cx = cy = size / 2
    r_out = size / 2 - 6
    r_in  = r_out * 0.58
    svg = (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{size}" height="{size}" viewBox="0 0 {size} {size}" '
        f'style="font-family:-apple-system,Segoe UI,Roboto,sans-serif;">'
    )
    import math as _m
    angle = -_m.pi / 2  # start oben
    for idx, item in enumerate(items):
        frac = float(item.get("raw") or 0) / total
        if frac <= 0:
            continue
        a0 = angle
        a1 = angle + frac * 2 * _m.pi
        angle = a1
        large = 1 if frac > 0.5 else 0
        x0o = cx + r_out * _m.cos(a0); y0o = cy + r_out * _m.sin(a0)
        x1o = cx + r_out * _m.cos(a1); y1o = cy + r_out * _m.sin(a1)
        x0i = cx + r_in  * _m.cos(a1); y0i = cy + r_in  * _m.sin(a1)
        x1i = cx + r_in  * _m.cos(a0); y1i = cy + r_in  * _m.sin(a0)
        color = palette[idx % len(palette)]
        d = (
            f"M {x0o:.1f} {y0o:.1f} "
            f"A {r_out} {r_out} 0 {large} 1 {x1o:.1f} {y1o:.1f} "
            f"L {x0i:.1f} {y0i:.1f} "
            f"A {r_in} {r_in} 0 {large} 0 {x1i:.1f} {y1i:.1f} Z"
        )
        svg += f'<path d="{d}" fill="{color}" stroke="#fff" stroke-width="1"/>'
    # Zentrumstext: Summe
    svg += (
        f'<text x="{cx}" y="{cy - 2}" font-size="13" fill="#666" text-anchor="middle">Σ</text>'
        f'<text x="{cx}" y="{cy + 14}" font-size="14" font-weight="600" fill="#333" text-anchor="middle">'
        f'{int(total):,}</text>'.replace(",", ".")
    )
    svg += "</svg>"
    # Legende als HTML-nachbearbeitbare Struktur
    legend = [
        {
            "label": str(i.get("label", "")),
            "value": str(i.get("display", "")),
            "color": palette[idx % len(palette)],
            "pct":   round(float(i.get("raw") or 0) / total * 100, 1),
        }
        for idx, i in enumerate(items)
    ]
    return {"type": "svg", "variant": "donut", "title": title, "svg": svg, "legend": legend}


def _build_chart_svg_sparkline(
    values: list[float] | list[dict],
    width: int = 100,
    height: int = 24,
    color: str = "#1976d2",
) -> dict:
    """
    Winziger Trend-Sparkline für KPI-Karten.
    values: Liste von floats oder dicts mit 'raw'-Schlüssel.
    """
    raw = []
    for v in values or []:
        if isinstance(v, dict):
            raw.append(float(v.get("raw") or 0))
        else:
            raw.append(float(v or 0))
    if len(raw) < 2:
        return {}
    vmax = max(raw); vmin = min(raw)
    span = (vmax - vmin) or 1.0
    n = len(raw)
    step = (width - 4) / max(n - 1, 1)
    pts = []
    for idx, v in enumerate(raw):
        x = 2 + idx * step
        y = 2 + (height - 4) - ((v - vmin) / span) * (height - 4)
        pts.append(f"{x:.1f},{y:.1f}")
    pl = " ".join(pts)
    svg = (
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}" '
        f'style="display:inline-block;vertical-align:middle;">'
        f'<polyline points="{pl}" fill="none" stroke="{color}" stroke-width="1.5" '
        f'stroke-linejoin="round" stroke-linecap="round"/>'
        f"</svg>"
    )
    return {"type": "svg", "variant": "sparkline", "svg": svg}


