# ─── Environmental Impact ────────────────────────────────────────────────────
#
# Umrechnungsfaktoren pro Seite (A4) basierend auf Industriedurchschnitten:
#   Paper:  ~5 g pro Blatt (80 g/m²)
#   Trees:  ~8333 A4-Blätter pro Baum (≈ 50 kg Reinpapier)
#   CO₂:    ~4.7 g pro Blatt (Produktion + Transport + Druck)
#   Water:  ~10 l pro Blatt (Zellstoff-Produktion)
#   Energy: ~50 Wh pro Blatt (Druck + Gerät)
#
# Bei Duplex-Seiten wird kein zusätzliches Blatt, aber die Hälfte pro
# "ausgegebene Seite" gezählt — das spart Papier/Bäume/Wasser, nicht aber
# Toner und Energie. Wir rechnen die Einsparung über `saved_sheets` direkt
# ab: jede eingesparte Seite zählt −100 % Papier/CO₂/Wasser/Bäume.

_ENV_FACTORS = {
    "paper_g_per_sheet":  5.0,        # Gramm pro A4-Blatt
    "sheets_per_tree":    8333.0,     # A4-Blätter pro Baum
    "co2_g_per_sheet":    4.7,        # Gramm CO₂ pro Blatt
    "water_l_per_sheet":  10.0,       # Liter Wasser pro Blatt
    "energy_wh_per_sheet":50.0,       # Wattstunden pro Blatt
}


def compute_env_impact(
    total_pages: int,
    duplex_pages: int = 0,
    saved_sheets: int = 0,
) -> dict[str, float]:
    """
    Berechnet die Umweltwirkung der ausgedruckten Seiten.

    Args:
        total_pages:  Gesamt gedruckte Seiten (Farb + S/W)
        duplex_pages: Davon Duplex-Seiten (zur Info, nicht direkt genutzt)
        saved_sheets: Eingesparte Blätter durch Duplex (positiv = gut)

    Returns:
        Dict mit den Schlüsseln: paper_kg, trees, co2_kg, water_l, energy_kwh.
        Alle Werte sind **nach** Abzug der Einsparungen durch Duplex.
    """
    tp = max(int(total_pages or 0), 0)
    ss = max(int(saved_sheets or 0), 0)
    # Netto-Blätter: total_pages ist seitenbasiert, saved_sheets ist blattbasiert.
    # Bei Duplex: 2 Seiten = 1 Blatt, sonst 1 Seite = 1 Blatt. Die Einsparung
    # sagt uns direkt, wieviele Blätter NICHT gedruckt wurden.
    physical_sheets = max(tp - ss, 0)

    paper_g   = physical_sheets * _ENV_FACTORS["paper_g_per_sheet"]
    trees     = physical_sheets / _ENV_FACTORS["sheets_per_tree"]
    co2_g     = physical_sheets * _ENV_FACTORS["co2_g_per_sheet"]
    water_l   = physical_sheets * _ENV_FACTORS["water_l_per_sheet"]
    energy_wh = physical_sheets * _ENV_FACTORS["energy_wh_per_sheet"]

    return {
        "paper_kg":    round(paper_g / 1000, 2),
        "trees":       round(trees, 3),
        "co2_kg":      round(co2_g / 1000, 2),
        "water_l":     round(water_l, 1),
        "energy_kwh":  round(energy_wh / 1000, 2),
        "sheets_used": physical_sheets,
        "sheets_saved": ss,
    }


def _build_env_impact_section(
    env: dict[str, float],
    labels: Optional[dict] = None,
) -> dict[str, Any]:
    """Baut eine Report-Section mit Umweltwirkungs-Metriken."""
    return {
        "title":   _lbl(labels, "rpt_eng_env_title"),
        "columns": [_lbl(labels, "rpt_eng_col_metric"), _lbl(labels, "rpt_eng_col_total")],
        "rows": [
            [_lbl(labels, "rpt_eng_env_paper_kg"), f"{env['paper_kg']:,.2f}"],
            [_lbl(labels, "rpt_eng_env_trees"),    f"{env['trees']:,.3f}"],
            [_lbl(labels, "rpt_eng_env_co2"),      f"{env['co2_kg']:,.2f}"],
            [_lbl(labels, "rpt_eng_env_water"),    f"{env['water_l']:,.0f}"],
            [_lbl(labels, "rpt_eng_env_energy"),   f"{env['energy_kwh']:,.2f}"],
        ],
        "chart": {},
    }


