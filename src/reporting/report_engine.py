def _translate_col(col: str, lang: str = "de") -> str:
    """Übersetzt einen DB-Spaltennamen in die aktive Sprache.
    Fallback: <lang> → Dialekt-Quelle → en → human-readable title case."""
    if not col:
        return ""
    col_key = str(col).strip()
    table = COLUMN_LABELS.get(lang)
    if table and col_key in table:
        return table[col_key]
    fb = _LANG_FALLBACK.get(lang)
    if fb and fb in COLUMN_LABELS and col_key in COLUMN_LABELS[fb]:
        return COLUMN_LABELS[fb][col_key]
    if "en" in COLUMN_LABELS and col_key in COLUMN_LABELS["en"]:
        return COLUMN_LABELS["en"][col_key]
    return col_key.replace("_", " ").strip().title()


# ─── Formatierungshilfen ──────────────────────────────────────────────────────

def _pdf_safe(text: str) -> str:
    """
    Ersetzt Unicode-Zeichen, die von fpdf2/Helvetica (Latin-1) nicht unterstützt werden.
    Verhindert FPDFUnicodeEncodingException bei Em-Dash, Sonderzeichen etc.
    """
    replacements = {
        "\u2013": "-",    # En-Dash –
        "\u2014": "-",    # Em-Dash —
        "\u2018": "'", "\u2019": "'",
        "\u201c": '"', "\u201d": '"',
        "\u2026": "...",
        "\u20ac": "EUR",
        "\u00b0": " Grad", "\u00b2": "2", "\u00b3": "3",
        "\u2122": "TM", "\u00ae": "(R)", "\u00a9": "(C)",
        "\u2022": "*", "\u25cf": "*",
    }
    for uni, asc in replacements.items():
        text = text.replace(uni, asc)
    return text.encode("latin-1", errors="replace").decode("latin-1")


def _fmt_num(v, decimals=0) -> str:
    if v is None:
        return "—"
    try:
        if decimals:
            return f"{float(v):,.{decimals}f}"
        return f"{int(v):,}"
    except (TypeError, ValueError):
        return str(v)


def _fmt_pct(v) -> str:
    if v is None:
        return "—"
    return f"{float(v):.1f} %"


def _fmt_cost(v, currency="€") -> str:
    if v is None:
        return "—"
    return f"{float(v):,.2f} {currency}"


def _fmt_delta(v):
    """Formatiert einen Delta-Wert als farbigen HTML-Span.
    Gibt Markup zurueck damit autoescape=True den HTML-Code nicht escaped."""
    if v is None:
        return "—"
    sign = "▲" if v > 0 else "▼"
    css  = "delta-pos" if v > 0 else "delta-neg"
    html = f'<span class="{css}">{sign} {abs(v):.1f}%</span>'
    try:
        from markupsafe import Markup
        return Markup(html)
    except ImportError:
        return html


